DROP TABLE IF EXISTS CUSTOMERS;
DROP TABLE IF EXISTS ORDERS;

-- create_customers_table.sql
CREATE TABLE CUSTOMERS (
    ID INTEGER PRIMARY KEY,
    NAME VARCHAR(255),
    SURNAME VARCHAR(255),
    AGE INTEGER,
    PHONE_NUMBER VARCHAR(20)
);
